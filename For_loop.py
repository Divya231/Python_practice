# For loop with range
# range(5)
# start=0
# condition<5=4
# increment +1

# range(1,6)
# start=1
# condition 1 to 5
# increment +1
# range(1,10,2)
# start =1
# condition 1 to 10
# increment +2

for num in range(10):
    print(num)

for num in range(1,10):
    print(num)
for num in range(3,10,2):
    print(num)